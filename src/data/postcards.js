// A PURELY VISUAL dataset with no business meaning. It is deliberately its own
// file rather than a slice of the gallery, the destinations or the packages:
// those lists are ordered and filtered for their own reasons, and a postcard
// wall borrowing from them silently changes whenever a product does. Adding a
// destination should never reshuffle the homepage, and cropping a postcard
// should never touch a sellable product.
//
// Nothing here is priced, linked or bookable. Each entry owns everything it
// needs to render:
//
//   id         stable React key
//   image      local WebP under public/images/ (AVIF/WebP variants are
//              generated by scripts/optimize-images.mjs)
//   captionEn  caption shown in English
//   captionId  caption shown in Indonesian
//   alt        alternative text for the photo itself
//   rotation   degrees of tilt on the board; the pinned-print look
//   order      render position, low to high
//
